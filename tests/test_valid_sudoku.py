from typing import List

import pytest

from src.ds.valid_sudoku import is_valid_sudoku


@pytest.mark.parametrize("inp, expected", [
    ([["5", "3", ".", ".", "7", ".", ".", ".", "."]
         , ["6", ".", ".", "1", "9", "5", ".", ".", "."]
         , [".", "9", "8", ".", ".", ".", ".", "6", "."]
         , ["8", ".", ".", ".", "6", ".", ".", ".", "3"]
         , ["4", ".", ".", "8", ".", "3", ".", ".", "1"]
         , ["7", ".", ".", ".", "2", ".", ".", ".", "6"]
         , [".", "6", ".", ".", ".", ".", "2", "8", "."]
         , [".", ".", ".", "4", "1", "9", ".", ".", "5"]
         , [".", ".", ".", ".", "8", ".", ".", "7", "9"]], True),
    ([["8", "3", ".", ".", "7", ".", ".", ".", "."]
         , ["6", ".", ".", "1", "9", "5", ".", ".", "."]
         , [".", "9", "8", ".", ".", ".", ".", "6", "."]
         , ["8", ".", ".", ".", "6", ".", ".", ".", "3"]
         , ["4", ".", ".", "8", ".", "3", ".", ".", "1"]
         , ["7", ".", ".", ".", "2", ".", ".", ".", "6"]
         , [".", "6", ".", ".", ".", ".", "2", "8", "."]
         , [".", ".", ".", "4", "1", "9", ".", ".", "5"]
         , [".", ".", ".", ".", "8", ".", ".", "7", "9"]], False)
])
def test_is_valid_sudoku(inp: List[List[str]], expected: bool):
    assert is_valid_sudoku(inp) == expected
